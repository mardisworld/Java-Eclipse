
public class Marissa_car {
	
	String name;
	String noise;
	
	
	
	//Methods
	
			
			//build setters
			public void setName(String name){
				this.name=name; //will set private variable
			}
			
			//build setters
			public void setNoise(String noise){
				this.noise=noise; //will set private variable
			}
			
			//and build getters 
			
			public String getName(){
				return this.name;
			}
			
			public String getNoise(){
				return this.noise;
			}

}
	

